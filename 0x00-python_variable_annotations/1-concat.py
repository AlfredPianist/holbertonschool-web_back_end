#!/usr/bin/env python3
"""Concat with anotations"""


def concat(str1: str, str2: str) -> str:
    """Concats two strings.
       Args:
           str1 (string): The first string.
           str2 (string): The second string.
       Returns:
           str: The concatenation of strings 1 and 2.
    """
    return str1 + str2
